export const INITIAL_DARKHOUSES = [];

export const refreshDarkhouses = () => {
    try {
        const saved = localStorage.getItem("haatza_darkhouses");
        const list = saved ? JSON.parse(saved) : INITIAL_DARKHOUSES_RAW;
        if (!saved) {
            localStorage.setItem("haatza_darkhouses", JSON.stringify(INITIAL_DARKHOUSES_RAW));
        }
        INITIAL_DARKHOUSES.length = 0;
        INITIAL_DARKHOUSES.push(...list);
    } catch {
        INITIAL_DARKHOUSES.length = 0;
        INITIAL_DARKHOUSES.push(...INITIAL_DARKHOUSES_RAW);
    }
};

// Trigger initial population
refreshDarkhouses();

